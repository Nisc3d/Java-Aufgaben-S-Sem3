public class Student {
    public int martikelnr;
    public double note;
    public double durchschnittsnote;

    Student(){
        durchschnittsnote = 0;
    }

    public int getMartikelnr() {
        return martikelnr;
    }

    public double getNote() {
        return note;
    }

    public double getDurchschnittsnote() {
        return durchschnittsnote;
    }

}
